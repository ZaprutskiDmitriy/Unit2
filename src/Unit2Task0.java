public class Unit2Task0 {
    public static void main(String[] args) {
        for (int i=0; i<=10; i++) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
        for (int j=0; j<11; j++) {
            System.out.print(j + " ");
        }
        System.out.println(" ");
        for (int k=1; k<=11; k++) {
            System.out.print((k-1) + " ");
        }
    }
}
